# String Funcions
Str1="VIJAY"
Str2="Chennai"
Str3="PinCode635001"
Str4="7373000055"
Str5=" "
Str6="int"
print("Check Alphabet          : ",Str1,":",Str1.isalpha())
print("Check Numeric           : ",Str4,":",Str4.isdigit())
print("Check Alpha Numeric     : ",Str3,":",Str3.isalnum())
print("Check identifier        : ",Str6,":",Str6.isidentifier())
print("Check Lower Case        : ",Str2,":",Str2.islower())
print("Check Upper Case        : ",Str1,":",Str1.isupper())
print("Check Title Case        : ",Str2,":",Str2.istitle())
print("Check BlankSpace        : ",Str5,":",Str5.isspace())
