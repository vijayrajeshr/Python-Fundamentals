#Arithmetic Operators

n1=int(input("Enter the First Number "))
n2=int(input("Enter the Second Number "))

print("\n Addition is  \t:",(n1+n2))

print("\n Subtract  is  \t:",(n1-n2))

print("\n Multiplication  is \t:",(n1*n2))

print("\n Divide is \t: ",(n1/n2))

print("\n Modulus is \t:",(n1%n2))

print(n1 ," Power ",n2 ," is \t:",(n1**n2))

print("Floor Division of " , n1," by ",n2 ," is \t:",(n1//n2))
