#Relational Operators

n1=int(input("Enter the First Number"))  
n2=int(input("Enter the Second Number")) 

print(n1>n2)                                

print("\n",n1," >= " ,n2, " : " ,(n1>=n2))

print("\n",n1," < " ,n2, " : " ,(n1<n2))
print("\n",n1," <= " ,n2, " : " ,(n1<=n2))
print("\n",n1," == " ,n2, " : " ,(n1==n2))
print("\n",n1," != " ,n2, " : " ,(n1!=n2))
