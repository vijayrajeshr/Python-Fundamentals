#Assignment Operators

n=100
print("\nThe Current value of n (assign n=100)\t:",n)

n+=10
print("\nThe Current value of n (assign n+=10)\t:",n)

n-=5
print("\nThe Current value of n (assign n-=5)\t:",n)

n*=5
print("\nThe Current value of n (assign n*=5)\t:",n)

n/=10
print("\nThe Current value of n (assign n/=10)\t:",n)

n%=7
print("\nThe Current value of n (assign n%=7)\t:",n)

n**=2
print("\nThe Current value of n (assign n**=10)\t:",n)

n//=2
print("\nThe Current value of n (assign n//=10)\t:",n)
